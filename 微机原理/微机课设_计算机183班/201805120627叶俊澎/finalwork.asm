    ; ;设置频率
	push dx 
    push ax
    push dx
    mov dx,12H
	mov ax,34DEH
	mov di,[ds:nowUseHz]
	div di
    mov [ds:nowUseHzTurned] , ax
    pop dx 
    pop ax
    pop dx
ret


