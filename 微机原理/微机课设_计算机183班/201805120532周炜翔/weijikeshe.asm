;*****************************************扬声器端口打开*****************************************
open_sound:;扬声器端口打开
	push ax

	in al,61H
	or al,00000011B;或操作与11111100B
	out 61H,al

	pop ax
	ret
;*****************************************扬声器端口打开*****************************************



